export function filterDrillObjects({ site, type, severity, nonGoodOnly }) {
  return drillObjects.filter((o) => {
    if (site && site !== "All" && o.site !== site) return false;
    if (type && type !== "All" && o.type !== type) return false;
    if (severity && severity !== "All" && o.severity !== severity) return false;
    if (nonGoodOnly && o.worstState === "Good") return false;
    return true;
  });
}

export function relatedFor(objectId) {
  return relatedByObject[objectId] || [];
}
